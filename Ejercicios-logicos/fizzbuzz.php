<?php
    // Logica
    for ($i = 1; $i <= 100; $i++) {
        $divisibleXTres = $i % 3 == 0;
        $divisibleXCinco = $i % 5 == 0;

        if ($divisibleXTres && $divisibleXCinco) {
            echo "fizzbuzz";
        } elseif ($divisibleXTres) {
            echo "fizz";
        } elseif ($divisibleXCinco) {
            echo "buzz";
        } else {
            echo $i;
        }
        echo "<br>";
    }
?>

    
